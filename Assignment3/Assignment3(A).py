import turtle

t = turtle.Turtle()

# 변수 i가 0부터 시작하므로 첫번째 루프는 아무 직선도 그려지지 않는다. 49개의 직선이 그려져야 하므로 50번 반복한다.
for i in range(50):
    # 루프의 횟수가 늘어날 수록(i의 값이 커질 수록),
    # 직선의 길이가 일정한 비율로 길어져야 하므로 10 * i 만큼의 길이의 직선을 그려야 한다.
    t.forward(10 * i)
    t.left(90)  # 사각형 모양으로 반시계방향으로 그려야 하니, 직선 하나를 그은 후 좌측으로 90도 돌아야 한다.

turtle.done()
